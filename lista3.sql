SELECT PRODUCT.NMPRODUCT, SUPPLIER.NMSUPPLIER, PRODUCT.VLPRICE, 
(VLPRICE-(VLPRICE* 0.1)) AS VLDESCT1, (VLPRICE-(VLPRICE* 0.2)) AS VLDESCT2,
(VLPRICE-(VLPRICE* 0.3)) AS VLDESCT3
FROM PRODUCT
INNER JOIN SUPPLIER ON PRODUCT.CDSUPPLIER=SUPPLIER.CDSUPPLIER
WHERE (VLPRICE-(VLPRICE* 0.1)) > 15
ORDER BY VLPRICE, NMPRODUCT

--b
SELECT PRODUCT.NMPRODUCT, SUPPLIER.NMSUPPLIER, 
PRODUCT.VLPRICE, (PRODUCT.VLPRICE* PRODUCT.QTSTOCK) AS TOTALSTOCK, 
((PRODUCT.QTSTOCK * 2)*PRODUCT.VLPRICE) AS TOTALSTOCK2
FROM PRODUCT
INNER JOIN SUPPLIER ON PRODUCT.CDSUPPLIER=SUPPLIER.CDSUPPLIER
WHERE  (PRODUCT.VLPRICE* PRODUCT.QTSTOCK) > 1200
ORDER BY SUPPLIER.NMSUPPLIER

