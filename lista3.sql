SELECT CUSTOMER.NMCUSTOMER, PRODUCT.NMPRODUCT, REQUEST.DTREQUEST, 
REQUEST.DTDELIVER, PRODUCTREQUEST.QTAMOUNT, PRODUCTREQUEST.VLUNITARY, REQUEST.VLTOTAL
FROM REQUEST
INNER JOIN CUSTOMER ON REQUEST.CDCUSTOMER = CUSTOMER.CDCUSTOMER
INNER JOIN PRODUCTREQUEST ON REQUEST.CDREQUEST = PRODUCTREQUEST.CDREQUEST
INNER JOIN PRODUCT ON PRODUCTREQUEST.CDPRODUCT = PRODUCT.CDPRODUCT

WHERE PRODUCTREQUEST.QTAMOUNT < 600
AND 
 REQUEST.DTREQUEST BETWEEN '2003-08-01' AND '2003-08-31'
AND PRODUCT.NMPRODUCT LIKE 'M%'


