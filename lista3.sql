SELECT CDCUSTOMER, NMCUSTOMER, IDFONE, NMADRESS
FROM CUSTOMER
WHERE IDFONE IS NOT NULL
AND NMCUSTOMER LIKE 'J%'
ORDER BY NMCUSTOMER

